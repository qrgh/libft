#include <stdlib.h>
#include "libft.h"

t_list		*ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	t_list	**tmp;

	del((*alst)->content, (*alst)->content_size);
	tmp = &((*alst)->next);
	free(*alst);
	*alst = *tmp;
	return (*alst);
}
