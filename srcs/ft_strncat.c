#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	index;

	index = ft_strlen(s1);
	while (*s2 && n)
	{
		s1[index] = *s2;
		s2++;
		++index;
		--n;
	}
	s1[index] = '\0';
	return (s1);
}
