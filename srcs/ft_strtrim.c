#include "libft.h"

char	*ft_strtrim(const char *s)
{
	int		i;

	if (!s)
		return (NULL);
	while (ft_isspace(*s))
		++s;
	i = ft_strlen(s) - 1;
	while (ft_isspace(s[i]))
		--i;
	return (ft_strsub(s, 0, i + 1));
}
