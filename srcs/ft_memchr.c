#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*s_tmp;

	s_tmp = ((unsigned char*)s);
	while (n--)
	{
		if ((*s_tmp) == c)
			return ((void*)s_tmp);
		++s_tmp;
	}
	return (NULL);
}
